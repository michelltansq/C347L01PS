package com.myapplicationdev.android.c347l01ps;

public class Holiday {
    private String holiday;
    private String date;

    public Holiday(String holiday, String date) {
        this.holiday = holiday;
        this.date = date;
    }

    public String getHoliday() {
        return holiday;
    }
    public String getDate() {
        return date;
    }
}